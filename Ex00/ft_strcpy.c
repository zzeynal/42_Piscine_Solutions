char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = 0;
	while(s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return(s1);
}


int	main()
{
	char dest[] = "hello";
	char source[] = "zeynal";
	ft_strcpy(dest, source);
	return 0;
}
